/**
 * Class that adds the wizard icon.
 *
 * @package	TYPO3
 * @subpackage	wildside_extbase
 */
class Tx_WildsideExtbase_Configuration_Wizard_Template {

	/**
	 * Processing the wizard items array
	 *
	 * @param	array		$wizardItems: The wizard items
	 * @return	Modified array with wizard items
	 */
	function proc($wizardItems)	{
		global $LANG;

		$LL = $this->includeLocalLang();

		$wizardItems['plugins_tx_wildsideextbase_template'] = array(
			'icon' => t3lib_extMgm::extRelPath('wildside_extbase') . 'Resources/Public/Icons/Plugin.png',
			'title' => 'Wildside Extbase Fluid Template Display',
			'description' => 'Displays any custom Fluid Template along with custom FluidVars',
			'params' => '&defVals[tt_content][CType]=list&defVals[tt_content][list_type]=wildsideextbase_template'
		);

		return $wizardItems;
	}

	/**
	 * Reads the [extDir]/locallang.xml and returns the $LOCAL_LANG array found in that file.
	 *
	 * @return	The array with language labels
	 */
	function includeLocalLang()	{
		$llFile = t3lib_extMgm::extPath('wildside_extbase').'Resources/Private/Language/locallang.xml';
		$LOCAL_LANG = t3lib_div::readLLXMLfile($llFile, $GLOBALS['LANG']->lang);

		return $LOCAL_LANG;
	}
}



